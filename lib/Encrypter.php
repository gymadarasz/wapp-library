class Encrypter
{
    /**
     * Method encrypt
     *
     * @param string $password password
     *
     * @return string
     */
    public function encrypt(string $password): string
    {
        return (string)password_hash($password, PASSWORD_BCRYPT, ['cost' => 12]);
    }
}
